"""Helpers potentially used in multiple modules."""


def preprocess(text):
    """Same preprocessing used as in pretrained models."""
    new_text = []
    for t in text.split(" "):
        t = "@user" if t.startswith("@") and len(t) > 1 else t
        t = "http" if t.startswith("http") else t
        new_text.append(t)
    return " ".join(new_text)
